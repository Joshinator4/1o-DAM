/*3. Escribe un programa que muestre por pantalla 10 palabras en inglés junto a su 
 * correspondiente traducción al castellano. Las palabras deben estar distribuidas en dos 
 */
package ejercicioEntradaSalida3;

public class EjercicioEntradaSalida3 {

	public static void main(String[] args) {
		// Realizamos las 2 columnas. Para igualar las palabras en inglés usamos espacios y luego usamos \t para separa las columnas 
		
		System.out.println("Inglés" + "\t" + "\t" + "Español");
		System.out.println();
		System.out.println("Hello " + "\t" + "\t" + "Hola");
		System.out.println("Bye   " + "\t" + "\t" + "Adiós");
		System.out.println("Good  " + "\t" + "\t" + "Bueno");
		System.out.println("Bad   " + "\t" + "\t" + "Malo");
		System.out.println("Dog   " + "\t" + "\t" + "Perro");
		System.out.println("Cow   " + "\t" + "\t" + "Vaca");
		System.out.println("Lion  " + "\t" + "\t" + "León");
		System.out.println("Tiger " + "\t" + "\t" + "Tigre");
		System.out.println("Fish  " + "\t" + "\t" + "Pescado");
		System.out.println("Bird  " + "\t" + "\t" + "Pájaro");

	}

}
